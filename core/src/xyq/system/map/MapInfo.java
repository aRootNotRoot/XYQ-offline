package xyq.system.map;

public class MapInfo {
	public int mapID;
	public String mapName;
	public String BGM;
	public String smallMAP_pack;
	public String smallMAP_was;
	public String tips;
	public int getMapID() {
		return mapID;
	}
	public void setMapID(int mapID) {
		this.mapID = mapID;
	}
	public String getMapName() {
		return mapName;
	}
	public void setMapName(String mapName) {
		this.mapName = mapName;
	}
	public String getBGM() {
		return BGM;
	}
	public void setBGM(String bGM) {
		BGM = bGM;
	}
	public String getSmallMAP_pack() {
		return smallMAP_pack;
	}
	public void setSmallMAP_pack(String smallMAP_pack) {
		this.smallMAP_pack = smallMAP_pack;
	}
	public String getSmallMAP_was() {
		return smallMAP_was;
	}
	public void setSmallMAP_was(String smallMAP_was) {
		this.smallMAP_was = smallMAP_was;
	}
	public String getTips() {
		return tips;
	}
	public void setTips(String tips) {
		this.tips = tips;
	}
	
	
}
